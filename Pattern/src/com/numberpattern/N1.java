package com.numberpattern;
//word 4
public class N1 {
	public static void main(String[] args) {
		for(int i=1; i<6; i++) // outer loop for rows
        { 
            
            for(int j=1; j<=i; j++) //  for rows
            { 
                
                System.out.print(i+ " "); 
    
                //incrementing value of num 
               
            } 
    
            // ending line after each row 
            System.out.println(); 
        } 
    } 
	}


