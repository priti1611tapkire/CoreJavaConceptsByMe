package com.secondaryreferance;

public class Employee {
	private int eid;
	private Name name;
	private double salary;
	private Mobileno mno;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public Name getName() {
		return name;
	}
	public void setName(Name name) {
		this.name = name;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary = salary;
	}
	public Mobileno getMno() {
		return mno;
	}
	public void setMno(Mobileno mno) {
		this.mno = mno;
	}
	

}
