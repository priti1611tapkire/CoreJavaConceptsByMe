package com.loops1;

public class Whileloop {
	public static void main(String[] args) {
		int x=1;
		while(x<=5){  //....x=1<=5...true x=2 2<=5...true..x=3<=5...true....x=4<=5...x=5<=5...true x=6!=5...false...then loop close
			System.out.println("value of x: "+x);//1, 2,3,4,5
			x++; //1+1=2...2+1=3...3+1=4....4+1=5...5+1=6
		}
	}

}
